// Example of postfix increment operator

var number = 25;
console.log("This value is before using postfix increament operator "+number);
// using postfix increament operator
console.log("This value is while using postfix increament operator "+number+"+1");
number++
console.log("This value is after using postfix increament operator "+number);


// Example of prefix increment operators
var number_prefix = 30;
alert("This value is before using prefix increament operator "+number_prefix);

alert("This value is while using prefix increament operator "+number_prefix);

++number_prefix;

alert("This value is after using prefix increament operator "+number_prefix);


